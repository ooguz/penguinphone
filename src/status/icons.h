/*
        List of Font Awesome icons and their unicode character ids:
                https://fortawesome.github.io/Font-Awesome/cheatsheet/

        Make sure that you have installed the latest version of Font Awesome
        on your system, to get all the icons. In debian900, you may use
        Debian Backports for that.

        Please keep the lists alphabetically sorted.

        Notes about single icons:

                clone:
                        Similar to the Maemo window switch icon.

                min_width:
                        This should be the widest icon, as it gets used to give
   all
                        icons a fixed width.

                times-circle:
                        Used as close-icon (x).

                th:
                        Similar to the Maemo menu icon.

*/

